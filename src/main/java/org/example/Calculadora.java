package org.example;

public class Calculadora {

    public double soma(double n1, double n2) {
        return n1 + n2;
    }

    public double subtrair(double n1, double n2) {
        return n1 - n2;
    }

    public double dividr(double n1, double n2) {
        return n1 / n2;
    }

    public double multiplicar(double n1, double n2) {
        return n1 * n2;
    }
}
